function y = plotXRR(alpha_0, fitmeas, beta_coeff, lambda, stddevrad, p)
  prodfactor = 10**(p(1)/10);
  sumterm = 10**(p(2)/10);
  p2 = p(3:end);
  d = p2(1:end/3);
  rho_e = p2(end/3+1:2*end/3);
  r = p2(2*end/3+1:end);
  Rtheor = xrrCurve(alpha_0, d, rho_e, beta_coeff, r, lambda, stddevrad)*prodfactor + sumterm;
  Rtheor = 10*log(Rtheor)/log(10);
  Rmeas = 10*log(fitmeas)/log(10);
  plot(alpha_0, Rtheor, alpha_0, Rmeas);
end

